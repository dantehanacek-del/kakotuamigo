// ─── HELPERS ────────────────────────────────────────────────────────────────

export function getItemById(id: string): CatalogItem | undefined {
  return catalogItems.find((i) => i.id === id);
}

export function getItemsByBrand(brand: string): CatalogItem[] {
  return catalogItems.filter((i) => i.brand === brand);
}

export function getItemsByCategory(cat: ItemCategory): CatalogItem[] {
  return catalogItems.filter((i) => i.category === cat);
}

export function getFeatured(): CatalogItem[] {
  return catalogItems.filter((i) => i.featured);
}

export function getAllBrands(): string[] {
  return [...new Set(catalogItems.map((i) => i.brand))].sort();
}

export function cnyToUsd(cny: number): string {
  return `~$${(cny / 7.2).toFixed(0)} USD`;
}

export function buildOrderMessage(items: CatalogItem[]): string {
  const lines = items.map(
    (item, idx) =>
      `${idx + 1}. *${item.name}*\n   Link: ${item.kakobuy_url}`
  );
  return [
    "🛍️ *ENCARGO — KAKOTUAMIGO*",
    "",
    ...lines,
    "",
    "Por favor indicame talle/color para cada item.",
  ].join("\n");
}

export function waOrderLink(items: CatalogItem[]): string {
  return `https://wa.me/${WHATSAPP_NUMBER}?text=${encodeURIComponent(
    buildOrderMessage(items)
  )}`;
}
